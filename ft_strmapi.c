#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	char	*p;
	size_t	i;

	if (!s || !f)
		return (0);
	i = 0;
	len = ft_strlen(s);
	p = malloc (len + 1);
	if (!p)
		return (0);
	while (i < len)
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
